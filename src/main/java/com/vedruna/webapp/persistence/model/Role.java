package com.vedruna.webapp.persistence.model;

/**
 * Enumeración que define roles para usuarios en la aplicación. Puede tener
 * valores ADMIN o USER.
 */
public enum Role {
	/**
	 * Rol que representa a un administrador.
	 */
	ADMIN,

	/**
	 * Rol que representa a un usuario estándar.
	 */
	USER
}
